	@Override
	public List<Seller> findByDepartment(Department department) {
		PreparedStatement st = null;
		ResultSet rs = null;
		try {
			st = conn.prepareStatement(
				"SELECT seller.*, department.Name as DepName "
				+ "FROM seller INNER JOIN department "
				+ "ON seller.DepartmentId = department.Id "
				+ "WHERE DepartmentId = ? "
				+ "ORDER BY Name"	
			);
			st.setInt(1, department.getId());
			
			rs = st.executeQuery();
			
			List<Seller> list = new ArrayList<>();
			Map<Integer, Department> map = new HashMap<>();
			
			while (rs.next()) {
				
//				Esse código vai buscar dentro do map se ja existe um
//				departamento de id 2, se não existir ele vai retornar
//				null e assim pode ser criado um novo departamento
				Department dep = map.get(rs.getInt("DepartmentId"));
				
//				Aqui esta sendo checado se ja existe um departamento
//				com o id 2, caso não exista esse departamento vai ser
//				instanciado e inserido dentro do map, assim impossibi-
//				litando a criação de outro departamento com o mesmo id
//				proposto na regra de negócio
				if (dep == null) {
					dep = instantiateDepartment(rs);
					map.put(rs.getInt("DepartmentId"), dep);
				}
				
				Seller obj = instantiateSeller(rs, dep);
				list.add(obj);
			}
			return list;
		}
		catch (SQLException e) {
			throw new DbException(e.getMessage());
		}
		finally {
			DB.closeStatement(st);
			DB.closeResultSet(rs);
		}
	}
	
